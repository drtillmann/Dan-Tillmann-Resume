$(document).ready(function(){
    $("#img-classes-scsu").click(function(){
        $("#classes-scsu").slideToggle("slow");
		if($("#img-classes-scsu").attr("src") == "images/cross_max.png"){
		$("#img-classes-scsu").attr("src", "images/minimize_min.png");
		}else{
			$("#img-classes-scsu").attr("src", "images/cross_max.png");
		};
		//alert("sliding!");
    });
});

