/** Custom Alert Box **/

function CustomDialog(){
		this.render = function(dialog){
			var winW = window.innerWidth;
			var winH = window.innerHeight;
			var dialogoverlay = document.getElementById('dialogoverlay');
			var dialogbox = document.getElementById('dialogbox');
			dialogoverlay.style.display = "block";
			dialogoverlay.style.height = winH+"px";
			
			dialogbox.style.left = (winW/2) - (550*.5) +"px";//this centers the dialog box no matter what size screen is used
			dialogbox.style.top = "100px";
			dialogbox.style.display = "block";
			
			document.getElementById('dialogboxhead').innerHTML = "Please Acknowledge This Message";
			document.getElementById('dialogboxbody').innerHTML = dialog;
			document.getElementById('dialogboxfoot').innerHTML = '<button id="btnOK" onclick="Alert.ok()">OK</button>';
			
		}
		
		this.ok = function(){
			document.getElementById('dialogbox').style.display = "none";
			document.getElementById('dialogoverlay').style.display = "none";
		}
}




















